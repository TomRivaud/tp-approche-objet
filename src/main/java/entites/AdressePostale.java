package entites;

public class AdressePostale {
    int numeroDeRue;
    String libelle;
    int codePostal;
    String ville;

    public AdressePostale(int numeroDeRue, String libelle, int codePostal, String ville){
        this.numeroDeRue = numeroDeRue;
        this.libelle = libelle;
        this.codePostal = codePostal;
        this. ville = ville;
    }
}
